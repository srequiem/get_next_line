#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		i;
	int		size;

	i = 0;
	size = ft_strlen(s1) + 1;
	if (!(res = (char*)malloc(size * sizeof(char))))
		return (0);
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
