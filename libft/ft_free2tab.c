#include "libft.h"

void	ft_free2tab(int **tab, size_t x)
{
	size_t i;

	if (!tab)
		return ;
	i = 0;
	while (i < x)
	{
		if (tab[i])
			free(tab[i]);
		i++;
	}
	free(tab);
}
