#include "libft.h"

void	ft_sort(int *tab, size_t size)
{
	size_t i;

	i = 0;
	while (i < size)
	{
		if (tab[i] > tab[i + 1])
		{
			ft_swap(&tab[i], &tab[i + 1]);
			i = 0;
		}
		else
			i++;
		size--;
	}
}
